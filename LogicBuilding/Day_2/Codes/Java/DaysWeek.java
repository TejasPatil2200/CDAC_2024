class DaysWeek {
	public static void main (String [] args){
		int Day=4;
		switch(Day){
			case 1:
			System.out.println("Its Monday and a Weekday..");
			break;
			case 2:
			System.out.println("Its Tuesday and a Weekday");
			break;
			case 3:
			System.out.println("Its Wednesday and a Weekday");
			break;
			case 4:
			System.out.println("Its Thursday and a Weekday");
			break;
			case 5:
			System.out.println("Its Friday and a Weekday");
			break;
			case 6:
			System.out.println("Its Saturday and a Weekend");
			break;
			case 7:
			System.out.println("Its Sunday and a Weekend");
			break;
			default:
			System.out.println("Not A VALID NO !!!");
		}
	}
}
